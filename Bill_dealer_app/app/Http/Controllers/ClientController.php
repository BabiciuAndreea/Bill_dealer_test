    {
        Client::create([
            'client_name' => $request->client_name,
            'cif' => $request->cif,
            'nr_reg' => $request->nr_reg,
            'city' => $request->city,
            'county' => $request->county,
            'address' => $request->address,
            'phone' => $request->phone,
            'email' => $request->email
        ]);
        return redirect(route('client.index'));
    }

